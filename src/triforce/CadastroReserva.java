    private void btn_confirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_confirmarMouseClicked
        // TODO add your handling code here:
        
        if(txt_datalcto.getText().length() > 0)
        {
            lbl_confirmarDataLcto.setVisible(false);
        }
        else
        {
            lbl_confirmarDataLcto.setVisible(true);
        }
        
        if(txt_datareserva.getText().length() > 0 )
        {
            lbl_confirmarDataReserva.setVisible(false);
        }
        else{
            lbl_confirmarDataReserva.setVisible(true);
        }
        
        if(txt_datalcto.getText().length() > 0 &&
                txt_datareserva.getText().length() > 0 )
        {
            JOptionPane.showMessageDialog(null, "Cadastro Feito com sucesso");
        }
    }//GEN-LAST:event_btn_confirmarMouseClicked

    
